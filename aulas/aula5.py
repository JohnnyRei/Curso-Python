"""
Tipos de operadores aritméticos:

+, -, *, /, //, **, %, ()

"""
print('adição +', 10 + 10)
print('subtração -', 10 - 10)
# Se eu multiplicar um int por str eu tenho uma repetição 10 * '10'.
print('multiplicação *', 10 + 10)
# Retorna um valor real.
print('divisão /', 10 / 10)
print('divisão de inteira //', 10 // 10)
# 2 elevado a 10 = 1014.
print('exponenciação **', 2 ** 10)
# Resto da divisão.
print('módulo %', 10 % 5)
# Altera a precedência.
print('()', (12+5)*10)
